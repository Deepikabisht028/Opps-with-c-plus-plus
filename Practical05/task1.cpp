#include<iostream>
using namespace std;
int main()
{ char ch='a';
  short s=-1;
  int i=335200;
  bool b=true;
  long l=34409899;
  float f=1.78f;
  long double ld=12.1;
  wchar_t wc=L'a';  
  cout<<"character variable     : "<<ch<<endl;
  cout<<"Short variable         : "<<s<<endl;
  cout<<"Integer variable       : "<<i<<endl;
  cout<<"Bool variable          : "<<b<<endl;
  cout<<"Long variable          : "<<l<<endl;
  cout<<"Float variable         : "<<f<<endl;
  cout<<"Long double            : "<<ld<<endl;
  cout<<"Wide character variable: "<<wc<<endl;
}
   
    
   
